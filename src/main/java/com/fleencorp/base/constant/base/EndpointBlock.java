package com.fleencorp.base.constant.base;

/**
 * Interface representing an endpoint block used in API requests.
 * This interface defines a method to retrieve the value representing the endpoint block.
 * Classes implementing this interface typically represent specific blocks of endpoints used in API requests.
 *
 * @author Yusuf Alamu Musa
 * @version 1.0
 */
public interface EndpointBlock {

  /**
   * Retrieves the value representing the endpoint block.
   *
   * @return The value representing the endpoint block
   */
  String value();
}

